// Arithmetic Operators
5 + 10; // 15
8 - 3; // 5
10 * 2; // 20
10 / 2; // 5
15 % 4; // 3

// Assignment Operators
let x = 10;
x += 5; // x = x + 5 => 15
x -= 5; // x = x - 5 => 10
x *= 5; // x = x * 5 => 50

// Comparison Operators
10 > 5; // true
10 < 5; // false
10 >= 10; // true
10 <= 5; // false
10 == 10; // true
10 === '10'; // false
10 != 10; // false
10 !== '10'; // true

// Logical Operators
true && false; // false
true || false; // true
!true; // false
